status_t read_fuse(ELEMU_Type *mu, uint32_t fuse, uint32_t *fuseData, size_t fuseLen)
{
    sscp_context_t sscpContext    = {0u};
    sss_mgmt_t mgmtContext        = {0u};
    sss_sscp_session_t sssSession = {0u};
    status_t status               = kStatus_Fail;

    /* Core Freq in MHz */
    mgmtContext.clockFrequency = CORE_CLK_FREQ / 1000000U;
    size_t optLen              = 4u;

    do
    {
        if (sscp_mu_init(&sscpContext, mu) != kStatus_SSCP_Success)
        {
            break;
        }
        /* OPEN SESSION */
        if (sss_sscp_open_session(&sssSession, 0u, kType_SSS_Ele200, &sscpContext) != kStatus_SSS_Success)
        {
            break;
        }

        if (sss_mgmt_context_init(&mgmtContext, &sssSession) != kStatus_SSS_Success)
        {
            break;
        }

        /* READ FUSE */
        if (sss_mgmt_fuse_read(&mgmtContext, fuse, fuseData, &fuseLen, (uintptr_t)&mgmtContext.clockFrequency,
                               &optLen) != kStatus_SSS_Success)
        {
            break;
        }

        /* If all steps before passes without break, then consider it as success*/
        status = kStatus_Success;

    } while (false);

    /* FREE TUNNEL CONTEXT */
    sss_mgmt_context_free(&mgmtContext);
    /* CLOSE SESSION */
    sss_sscp_close_session(&sssSession);

    return status;
}

status_t load_and_decrypt(ELEMU_Type *mu, uint32_t image[])
{
    uint32_t i, j = 0;
    uint32_t resultState          = 0u;
    sss_sscp_tunnel_t tunnelCtx   = {0u};
    sss_sscp_session_t sssSession = {0u};
    sscp_context_t sscpContext    = {0u};
    status_t status               = kStatus_Fail;

    nboot_sb3_header_t *block0 = (nboot_sb3_header_t *)image;

    do
    {
        if (sscp_mu_init(&sscpContext, mu) != kStatus_SSCP_Success)
        {
            break;
        }
        /* OPEN SESSION */
        if (sss_sscp_open_session(&sssSession, 0u, kType_SSS_Ele200, &sscpContext) != kStatus_SSS_Success)
        {
            break;
        }

        if ((block0->imageType & NBOOT_IMAGE_TYPE_MASK) != kNBOOT_ImageTypeSb3)
        {
            break;
        }

        /* OPEN TUNNEL */
        if (sss_sscp_tunnel_context_init(&tunnelCtx, &sssSession, TUNNEL_TYPE_SB3_FILE_AUTH) != kStatus_SSS_Success)
        {
            break;
        }

        /* UPLOAD FIRST BLOCK */
        if (sss_sscp_tunnel(&tunnelCtx, (uint8_t *)image, block0->imageTotalLength, &resultState) !=
            kStatus_SSS_Success)
        {
            break;
        }

        /* UPLOAD OTHER BLOCKS */
        for (i = 0; i < block0->blockCount; i++)
        {
            PRINTF("Encrypted SB3 block %d: \r\n", i + 1u);
            /* Print data*/
            PRINT_DATA;
            PRINTF("\r\n");
            if (sss_sscp_tunnel(&tunnelCtx, &((uint8_t *)image)[(i * block0->blockSize) + block0->imageTotalLength],
                                block0->blockSize, &resultState) != kStatus_SSS_Success)
            {
                break;
            }
            if (resultState != TUNNEL_SUCCESS)
            {
                PRINTF("Tunnel is failed to be executed");
                break;
            }

            PRINTF("Decrypted SB3 block %d: \r\n", i + 1u);

            /* Print data*/

            PRINT_DATA;
            PRINTF("\r\n");
        }
        if (i == block0->blockCount)
        {
            status = kStatus_Success;
        }
    } while (false);

    /* FREE TUNNEL CONTEXT */
    sss_sscp_tunnel_context_free(&tunnelCtx);
    /* CLOSE SESSION */
    sss_sscp_close_session(&sssSession);

    return status;
}

